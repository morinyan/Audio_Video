/**

 1. 音频信号分为: 模拟信号，数字信号
 
 2. 音频的两个重要指标: 采样频率，比特率
 
 采样频率是单位时间内，采集的点的数量，记录的是声音的快慢的指标
 
 采样频率比较常用的有: 44.1kHz，它的意思是每秒取样44100次, 48kHz, 96kHz
 
 比特率，又叫振幅，记录的是分贝，声音的大小的指标
 
 数码录音一般使用16比特、20比特或24比特, 16比特就是指把波形的振幅划为2^16即65536个等级
 
 16比特时，动态范围是96dB
 20比特时，动态范围是120dB
 24比特时，动态范围是144dB
 


*/
